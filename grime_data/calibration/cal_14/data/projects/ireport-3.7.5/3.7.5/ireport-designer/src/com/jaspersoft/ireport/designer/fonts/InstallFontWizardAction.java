package com.jaspersoft.ireport.designer.fonts;

import org.openide.util.HelpCtx;
import org.openide.util.actions.CallableSystemAction;

// An example action demonstrating how the wizard could be called from within
// your code. You can copy-paste the code below wherever you need.
public final class InstallFontWizardAction extends CallableSystemAction {


    public void performAction() {
        InstallFontWizardDescriptor wizardDescriptor = new InstallFontWizardDescriptor();
        wizardDescriptor.runWizard();
    }

    

    public String getName() {
        return "Install Font";
    }

    @Override
    public String iconResource() {
        return null;
    }

    public HelpCtx getHelpCtx() {
        return HelpCtx.DEFAULT_HELP;
    }

    @Override
    protected boolean asynchronous() {
        return false;
    }
}
